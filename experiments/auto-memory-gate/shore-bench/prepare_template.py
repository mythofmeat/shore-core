def make_character_md(name: str, partner: str) -> str:
    return (
        f"You are {name}, in a long-running conversation with your friend {partner}. "
        f"{partner} has just asked you a factual question about shared history or personal details you've discussed. "
        f"Use the memory tool to recall what you know if the answer is not in the recent conversation. "
        f"Answer with the SHORTEST POSSIBLE factual response — usually 1 to 5 words. "
        f"Match the question's format: 'when' → a date or year; 'what' → a noun or noun phrase; "
        f"yes/no questions → 'Yes' or 'No'. "
        f"Do NOT preface with 'Based on our conversation' or similar filler. Just the answer tokens. "
        f"If you genuinely cannot recall the answer, respond with 'not available'."
    )


def reindex_template(template_dir: Path, char_name: str):
    """Spawn daemon pointed at template, run `shore memory reindex`, tear down.

    This populates the Lance vector store by calling the configured embedding
    model over every Entry row in memory.db. One-time cost at template prep;
    the driver's per-question tmpdir copy inherits the vectors.
    """
    env = os.environ.copy()
    env["SHORE_CONFIG_DIR"] = str(template_dir / "config")
    env["SHORE_DATA_DIR"] = str(template_dir / "data")
    env["SHORE_RUNTIME_DIR"] = str(template_dir / "runtime")
    instance_id = f"bench-reindex-{char_name}"
    daemon_log = template_dir / "runtime" / "reindex.log"
    daemon_log.parent.mkdir(parents=True, exist_ok=True)

    with open(daemon_log, "w") as log_f:
        daemon = subprocess.Popen(
            [str(DAEMON_BIN), "--instance-id", instance_id, "--addr", "127.0.0.1:0"],
            env=env, stdout=log_f, stderr=subprocess.STDOUT, stdin=subprocess.DEVNULL,
        )
    try:
        addr = None
        instances_path = template_dir / "runtime" / "instances.json"
        deadline = time.time() + 10.0
        while time.time() < deadline:
            if instances_path.exists():
                try:
                    data = json.loads(instances_path.read_text())
                    if isinstance(data, list) and data:
                        for entry in data:
                            if entry.get("id") == instance_id and entry.get("addr"):
                                addr = entry["addr"]
                                break
                    if addr:
                        break
                except (json.JSONDecodeError, OSError):
                    pass
            time.sleep(0.1)
        if addr is None:
            raise RuntimeError(f"daemon for {char_name} did not register within 10s")

        result = subprocess.run(
            [str(SHORE_BIN), "--addr", addr, "--character", char_name, "memory", "reindex"],
            env=env, capture_output=True, text=True, timeout=120,
        )
        if result.returncode != 0:
            raise RuntimeError(
                f"reindex failed for {char_name}:\n  stdout: {result.stdout[:400]}\n  stderr: {result.stderr[:400]}"
            )
        print(f"  reindex     : {result.stdout.strip().splitlines()[-1] if result.stdout.strip() else 'done'}")
    finally:
        daemon.terminate()
        try:
            daemon.wait(timeout=5)
        except subprocess.TimeoutExpired:
            daemon.kill()
            daemon.wait()
        # Drop instances.json so the next run starts clean
        if (template_dir / "runtime" / "instances.json").exists():
            (template_dir / "runtime" / "instances.json").unlink()


def bootstrap_template(conv_id: str):
    info = CHAR_BY_CONV.get(conv_id)
    if info is None:
        print(f"ERROR: no character mapping for {conv_id}", file=sys.stderr)
        sys.exit(1)
    char_name = info["name"]
    partner = info["partner"]

    src = FIXTURES / conv_id
    if not (src / "memory.db").exists() or not (src / "active.jsonl").exists():
        print(f"ERROR: fixtures missing for {conv_id}. Run prepare_fixtures.py first.", file=sys.stderr)
        sys.exit(1)

    dest = TEMPLATES / conv_id
    if dest.exists():
        shutil.rmtree(dest)

    # config/
    (dest / "config").mkdir(parents=True)
    (dest / "config" / "config.toml").write_text(make_config_toml())
    (dest / "config" / "characters" / char_name).mkdir(parents=True)
    (dest / "config" / "characters" / char_name / "character.md").write_text(
        make_character_md(char_name, partner)
    )

    # data/
    char_data = dest / "data" / char_name
    (char_data / "memory").mkdir(parents=True)
    shutil.copy(src / "memory.db", char_data / "memory" / "memory.db")
    shutil.copy(src / "active.jsonl", char_data / "active.jsonl")
    (char_data / "autonomy_state.json").write_text("{}")

    # runtime/
    (dest / "runtime").mkdir(parents=True)

    print(f"{conv_id}:")
    print(f"  character   : {char_name} (talking to {partner})")
    print(f"  template    : {dest}")
    print(f"  memory.db   : {(char_data / 'memory' / 'memory.db').stat().st_size} bytes")
    print(f"  active.jsonl: {(char_data / 'active.jsonl').stat().st_size} bytes")

    reindex_template(dest, char_name)


def main():
    if not FIXTURES.exists():
        print("ERROR: fixtures dir not found. Run prepare_fixtures.py first.", file=sys.stderr)
        sys.exit(1)
    print(f"profile: {PROFILE}  (templates → {TEMPLATES})")
    for conv_id in CONVS:
        bootstrap_template(conv_id)


if __name__ == "__main__":
    main()
