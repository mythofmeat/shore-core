def reindex(template_dir: Path, char_name: str):
    env = os.environ.copy()
    env["SHORE_CONFIG_DIR"] = str(template_dir / "config")
    env["SHORE_DATA_DIR"] = str(template_dir / "data")
    env["SHORE_RUNTIME_DIR"] = str(template_dir / "runtime")
    instance_id = f"realistic-reindex-{char_name}"
    log_path = template_dir / "runtime" / "reindex.log"
    log_path.parent.mkdir(parents=True, exist_ok=True)

    with open(log_path, "w") as log_f:
        daemon = subprocess.Popen(
            [str(DAEMON_BIN), "--instance-id", instance_id, "--addr", "127.0.0.1:0"],
            env=env, stdout=log_f, stderr=subprocess.STDOUT, stdin=subprocess.DEVNULL,
        )
    try:
        addr = None
        deadline = time.time() + 10.0
        ipath = template_dir / "runtime" / "instances.json"
        while time.time() < deadline:
            if ipath.exists():
                try:
                    data = json.loads(ipath.read_text())
                    if isinstance(data, list):
                        for e in data:
                            if e.get("id") == instance_id and e.get("addr"):
                                addr = e["addr"]
                                break
                    if addr:
                        break
                except (json.JSONDecodeError, OSError):
                    pass
            time.sleep(0.1)
        if addr is None:
            raise RuntimeError(f"daemon did not register for {char_name}")

        result = subprocess.run(
            [str(SHORE_BIN), "--addr", addr, "--character", char_name, "memory", "reindex"],
            env=env, capture_output=True, text=True, timeout=120,
        )
        if result.returncode != 0:
            raise RuntimeError(
                f"reindex failed for {char_name}: {result.stderr[:400]}"
            )
        last = result.stdout.strip().splitlines()[-1] if result.stdout.strip() else "done"
        print(f"  reindex     : {last}")
    finally:
        daemon.terminate()
        try:
            daemon.wait(timeout=5)
        except subprocess.TimeoutExpired:
            daemon.kill()
            daemon.wait()
        if (template_dir / "runtime" / "instances.json").exists():
            (template_dir / "runtime" / "instances.json").unlink()


def bootstrap(conv_id: str):
    info = CHAR_BY_CONV.get(conv_id)
    if info is None:
        sys.exit(f"no character mapping for {conv_id}")
    char_name = info["name"]
    card = info["card"]

    src = FIXTURES / conv_id
    if not (src / "memory.db").exists() or not (src / "active.jsonl").exists():
        sys.exit(f"fixtures missing for {conv_id}; run prepare_fixtures.py first")
    if not card.exists():
        sys.exit(f"character card missing: {card}")

    dest = TEMPLATES / conv_id
    if dest.exists():
        shutil.rmtree(dest)

    (dest / "config").mkdir(parents=True)
    (dest / "config" / "config.toml").write_text(make_config_toml())
    (dest / "config" / "characters" / char_name).mkdir(parents=True)
    shutil.copy(card, dest / "config" / "characters" / char_name / "character.md")

    char_data = dest / "data" / char_name
    (char_data / "memory").mkdir(parents=True)
    shutil.copy(src / "memory.db", char_data / "memory" / "memory.db")
    shutil.copy(src / "active.jsonl", char_data / "active.jsonl")
    (char_data / "autonomy_state.json").write_text("{}")

    (dest / "runtime").mkdir(parents=True)

    print(f"{conv_id}:")
    print(f"  character   : {char_name}")
    print(f"  template    : {dest}")

    reindex(dest, char_name)


def main():
    print(f"realistic template → {TEMPLATES}")
    for c in CONVS:
        bootstrap(c)


if __name__ == "__main__":
    main()
