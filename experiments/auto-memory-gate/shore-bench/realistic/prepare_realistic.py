"""
Build the *realistic* bench template.

Differences from the direct-query bench (prepare_template.py):
  - Long, natural character prompts (character_<name>.md from this dir).
  - No tool-use hints in the character prompt.
  - All text-mode tools enabled.
  - Opus-4.7 as chat model — the failure surface the user actually experiences.
  - Other memory slots mirror the prod profile (with the corrected role swap:
    Gemma-4 on memory_agent, Minimax on tool_model).

Outputs templates to `templates/realistic/<conv_id>/`. Reindexes vectors via
`shore memory reindex` so Lance matches the config's embedding model.
"""

